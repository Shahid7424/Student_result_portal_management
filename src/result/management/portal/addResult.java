    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String roll=jTextField1.getText();
       String physics=jTextField2.getText();
       String maths=jTextField3.getText();
       String dbms=jTextField4.getText();
       String os=jTextField5.getText();
       String cn=jTextField6.getText();

       
         try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root", "PHW#84#id");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,maths,dbms,os,cn) values('"+roll+"','"+physics+"','"+maths+"','"+dbms+"','"+os+"','"+cn+"')");
                JOptionPane.showMessageDialog(null, "Marks saved successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "This roll number is registered!");
            }
       
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(null, e.toString());
                
        }      
    }//GEN-LAST:event_jButton1ActionPerformed

